	$json = get($url);
	
	if ($json == '')
	{
		echo "DOI $doi not found\n";
	
	}
	else
	{
		$reference = json_decode($json);
		
		//print_r($reference);
		
		// Do we have a PMID?
		
		$have_pmid = false;
		
		if (isset($reference->identifier))
		{
			foreach ($reference->identifier as $identifier)
			{
				if ($identifier->type == 'pmid')
				{
					$have_pmid = true;
				}
			}
		}
		
		if ($have_pmid)
		{
			echo "Have PMID\n";
		}
		else
		{
			$pmid = doi2pmid($doi);
			
			
			
