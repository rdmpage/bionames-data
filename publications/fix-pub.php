	if ($json != '')
	{
		$reference = json_decode($json);
		
		$count = 0;
		
		//print_r($reference);
		
		$obj = new stdclass;
		$obj->sici = $sici;
		
		if (isset($reference->identifier))
		{
			foreach ($reference->identifier as $identifier)
			{
				$k = $identifier->type;
				$v = $identifier->id;
			
				switch ($identifier->type)
				{
					case 'pmc':
						$v = str_replace('pmc', '', $v);
						break;
					
					default:
						break;
				}
				$obj->{$k} = $v;
				
				$count++;
			}
		}
		
		if (isset($reference->link))
		{
			foreach ($reference->link as $link)
			{
				$k = $link->anchor;
				$v = $link->url;
			
				$count++;
				
				switch ($link->anchor)
				{
					case 'PDF':
						$obj->pdf = $v;
						break;

					case 'LINK':
						$obj->url = $v;
						break;
					
					default:
						break;
				}
				
				
			}
		}
		
		
		if (isset($reference->journal))
		{
			if (isset($reference->journal->identifier))
			{
				$count++;
				
				foreach ($reference->journal->identifier as $identifier)
				{
					$k = $identifier->type;
					$v = $identifier->id;
					$obj->{$k} = $v;
				}
			}
		}
		
				
		//print_r($obj);
		
		if ($count > 0)
		{
			$pairs = array();
			foreach ($obj as $k => $v)
			{
				if ($k != 'sici')
				{
					$pairs[] = $k . '="' . $v . '"';
				}
			}
		
			$sql = 'UPDATE names SET ' . join("," , $pairs) . ' WHERE sici="' . $obj->sici . '";';
			echo $sql . "\n";
		}		
	}
